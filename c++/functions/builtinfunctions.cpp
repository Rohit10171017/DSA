#include<iostream>
#include<cmath>
using namespace std ;
int main()
{
    int n,m;
    cout<<"enter value of n : ";
    cin>>n;
    cout<<"enter value of m : ";
    cin>>m;
    cout<<"maximum number out of two input numbers is : "<<max(n,m)<<endl;
    cout<<"minimum number out of two input numbers is : "<<min(n,m)<<endl;
    cout<<"square root of "<<n<<" is = "<<sqrt(n)<<endl;
    cout<<"square root of "<<m<<" is = "<<sqrt(m)<<endl;
    return 0;
    
}